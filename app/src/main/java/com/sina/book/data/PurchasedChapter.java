package com.sina.book.data;

/**
 * @ClassName: PurchasedChapter
 * @Description: 购买章节信息
 * @author Li Wen
 * @date 2012-11-15
 * 
 */
public class PurchasedChapter {
    /**
     * 购买章节ID
     */
    private String chapterId;
    /**
     * 购买章节名称
     */
    private String chapterName;
    /**
     * 购买章节价格
     */
    private String chapterPrice;
    /**
     * 章节购买时间
     */
    private String purchasedTime;
    /**
     * 章节所在书Id
     */
    private String bookId;
    /**
     * 章节所在书名称
     */
    private String bookName;

    private String productType;

    public String getChapterId() {
        return chapterId;
    }

    public void setChapterId(String chapterId) {
        this.chapterId = chapterId;
    }

    public String getChapterName() {
        return chapterName;
    }

    public void setChapterName(String chapterName) {
        this.chapterName = chapterName;
    }

    public String getChapterPrice() {
        return chapterPrice;
    }

    public void setChapterPrice(String chapterPrice) {
        this.chapterPrice = chapterPrice;
    }

    public String getPurchasedTime() {
        return purchasedTime;
    }

    public void setPurchasedTime(String purchasedTime) {
        this.purchasedTime = purchasedTime;
    }

    public String getBookId() {
        return bookId;
    }

    public void setBookId(String bookId) {
        this.bookId = bookId;
    }

    public String getBookName() {
        return bookName;
    }

    public void setBookName(String bookName) {
        this.bookName = bookName;
    }

    public String getProductType() {
        return productType;
    }

    public void setProductType(String productType) {
        this.productType = productType;
    }

    @Override
    public String toString() {
        return "PurchasedChapter [chapterId=" + chapterId + ", chapterName=" + chapterName
                + ", chapterPrice=" + chapterPrice + ", purchasedTime=" + purchasedTime
                + ", bookId=" + bookId + ", bookName=" + bookName + ", productType=" + productType
                + "]";
    }

}
