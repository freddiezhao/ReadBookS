package com.sina.book.data;

/**
 * 名人推荐书单
 * 
 * @author Tsimle
 * 
 */
public class FamousRecommend {
    private String uid;
    private String headUrl;
    private String screenName;
    private int listCount;
    private String recoBookTitle;
    private String recoBookIntro;
    private String desc;

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getHeadUrl() {
        return headUrl;
    }

    public void setHeadUrl(String headUrl) {
        this.headUrl = headUrl;
    }

    public String getScreenName() {
        return screenName;
    }

    public void setScreenName(String screenName) {
        this.screenName = screenName;
    }

    public int getListCount() {
        return listCount;
    }

    public void setListCount(int listCount) {
        this.listCount = listCount;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getRecoBookTitle() {
        return recoBookTitle;
    }

    public void setRecoBookTitle(String recoBookTitle) {
        this.recoBookTitle = recoBookTitle;
    }

    public String getRecoBookIntro() {
        return recoBookIntro;
    }

    public void setRecoBookIntro(String recoBookIntro) {
        this.recoBookIntro = recoBookIntro;
    }

}
