package com.sina.book.data;

/**
 * @ClassName: UserInfo
 * @Description: 用户信息
 * @author Li Wen
 * @date 2012-11-6
 * 
 */
public class UserInfo {
    private String name;
    private String profile_image_url;
    private String gsid;
    private String gsidExpire;
    private String autoDownBid;

    public String getuName() {
        return name;
    }
    
    public void setuName(String name) {
    	if(name != null && name.length() > 0){
    		this.name = name;
    	}
    }

    public void setUserProfileUrl(String url) {
        this.profile_image_url = url;
    }

    public String getUserProfileUrl() {
        return profile_image_url;
    }

    public String getGsid() {
        return gsid;
    }

    public void setGsid(String gsid) {
        this.gsid = gsid;
    }

    public String getGsidExpire() {
        return gsidExpire;
    }

    public void setGsidExpire(String gsidExpire) {
        this.gsidExpire = gsidExpire;
    }

    public String getAutoDownBid() {
        return autoDownBid;
    }

    public void setAutoDownBid(String autoDownBid) {
        this.autoDownBid = autoDownBid;
    }
}
