package com.sina.book.data;

import java.util.ArrayList;

/**
 * 热门分类
 */
public class RecommendCateResult {
    private String title;
    private String name;

    private ArrayList<RecommendCateItem> datas = new ArrayList<RecommendCateItem>();

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public ArrayList<RecommendCateItem> getDatas() {
        return datas;
    }

    public void setDatas(ArrayList<RecommendCateItem> datas) {
        this.datas = datas;
    }

    public void addData(RecommendCateItem item) {
        this.datas.add(item);
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
