package com.sina.book.data;

import java.util.ArrayList;
import java.util.HashMap;

/**
 * 存储通用活动数据类
 * 
 * @author chenjianli
 * 
 */
public class GeneralActivityInfo {

	private boolean isShow;// 是否显示
	private int showNum;// 显示次数
	private int actId;// 活动ID
	private String title;// 活动标题
	private String subTitle;// 活动内容
	private String imgUrl;// 活动图片(直接由Dialog展示)
	private String activityUrl;// 活动链接

	private ArrayList<HashMap<String, String>> mButtons;

	public ArrayList<HashMap<String, String>> getButtons() {
		if (mButtons == null) {
			mButtons = new ArrayList<HashMap<String, String>>();
		}
		return mButtons;
	}

	public boolean isShow() {
		return isShow;
	}

	public void setShow(boolean isShow) {
		this.isShow = isShow;
	}

	public int getShowNum() {
		// return 100;
		return showNum;
	}

	public void setShowNum(int showNum) {
		this.showNum = showNum;
	}

	public int getActId() {
		// return 2;
		return actId;
	}

	public void setActId(int actId) {
		this.actId = actId;
	}

	public String getSubTitle() {
		return subTitle;
	}

	public void setSubTitle(String subTitle) {
		this.subTitle = subTitle;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getImgUrl() {
		return imgUrl;
	}

	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}

	public String getActivityUrl() {
		return activityUrl;
	}

	public void setActivityUrl(String activityUrl) {
		this.activityUrl = activityUrl;
	}

}
