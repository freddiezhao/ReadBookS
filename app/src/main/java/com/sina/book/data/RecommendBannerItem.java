package com.sina.book.data;

import java.util.ArrayList;

public class RecommendBannerItem {
	/** 包月Banner. */
	public static final String BANNER_MONTH = "month";
	/** 员工推荐Banner. */
	public static final String BANNER_STAFF = "staff";
	/** 推荐单本书籍Banner. */
	public static final String BANNER_ONE = "one";
	/** 推荐书籍列表Banner. */
	public static final String BANNER_LIST = "list";
	/** 推荐专题Banner. */
	public static final String BANNER_TWO = "two";
	/** 推荐活动Banner. */
	public static final String BANNER_THREE = "three";

	private String type;
	private String tags;
	private String imageUrl;
	private String itemName;
	private int imageResId;
	private int total;
	private ArrayList<Book> books;

	private int topicId = -1;

	private String url;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getItemName() {
		return itemName;
	}

	public void setItemName(String itemName) {
		this.itemName = itemName;
	}

	public String getTags() {
		return tags;
	}

	public void setTags(String tags) {
		this.tags = tags;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	public int getImageResId() {
		return imageResId;
	}

	public void setImageResId(int imageResId) {
		this.imageResId = imageResId;
	}

	public ArrayList<Book> getBooks() {
		return books;
	}

	public void setBooks(ArrayList<Book> books) {
		this.books = books;
	}

	public void setTopicId(int id) {
		this.topicId = id;
	}

	public int getTopicId() {
		return topicId;
	}

	public String getUrl() {
		return url;
	}

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	public void setUrl(String url) {
		this.url = url;
	}
}
