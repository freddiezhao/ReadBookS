package com.sina.book.data;

import java.util.ArrayList;

public class RecommendMonthResult {
    private String name;

    private ArrayList<RecommendMonthItem> datas = new ArrayList<RecommendMonthItem>();

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public ArrayList<RecommendMonthItem> getDatas() {
        return datas;
    }

    public void setDatas(ArrayList<RecommendMonthItem> datas) {
        this.datas = datas;
    }
    
    public void addData(RecommendMonthItem item) {
        this.datas.add(item);
    }

}
