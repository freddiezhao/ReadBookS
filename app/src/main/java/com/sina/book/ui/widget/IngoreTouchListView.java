package com.sina.book.ui.widget;

import android.content.Context;
import android.util.AttributeSet;
import android.view.MotionEvent;
import android.widget.ListView;

/**
 * 忽略Touch事件的ListView
 * 
 * @author MarkMjw
 * @date 2013-4-16
 */
public class IngoreTouchListView extends ListView {

    public IngoreTouchListView(Context context) {
        super(context);
    }

    public IngoreTouchListView(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    public IngoreTouchListView(Context context, AttributeSet attrs, int defStyle) {
        super(context, attrs, defStyle);
    }

    @Override
    public boolean onTouchEvent(MotionEvent ev) {
        return false;
    }

}
