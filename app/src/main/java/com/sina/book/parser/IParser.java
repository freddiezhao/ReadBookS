package com.sina.book.parser;

import java.io.InputStream;

/**
 * 解析器接口
 * 
 */
public interface IParser {

    /**
     * 解析
     * 
     * @param in
     *            ：被解析的输入流
     */
    public Object parse(InputStream in);

    /**
     * 解析字符串
     * @param jsonString
     * @return
     */
    public Object parseString(String jsonString);

    public String getCode();

    public String getMsg();
}
